Set1A = {'blue', 'green'}
Set2A = {'blue', 'yellow'}


sym_diff_A = Set1A.symmetric_difference(Set2A)
print("Symmetric difference for Set1A and Set2A:", sym_diff_A)


Set1B = {1, 2, 3, 4, 5}
Set2B = {1, 5, 6, 7, 8, 9}


sym_diff_B = Set1B.symmetric_difference(Set2B)
print("Symmetric difference for Set1B and Set2B:", sym_diff_B)